def decode_qrcode(qrcode):
    def get_bits():
        c = 20
        up = True
        
        # Проходимо стовпці парами з права на ліво
        while c > 0:
            if c == 6:
                c -= 1  # Пропускаємо вертикальну смугу синхронізації
                
            rows = range(20, -1, -1) if up else range(0, 21)
            for r in rows:
                if r == 6:
                    continue  # Пропускаємо горизонтальну смугу синхронізації
                    
                for col in (c, c - 1):
                    # Пропускаємо зони позиціонування та інформацію про формат
                    if col <= 8 and r <= 8:  # Верхній лівий кут
                        continue
                    if col <= 8 and r >= 13: # Нижній лівий кут
                        continue
                    if col >= 13 and r <= 8: # Верхній правий кут
                        continue
                        
                    # Отримуємо значення пікселя (1 - чорний, 0 - білий)
                    raw = qrcode[r][col]
                    
                    # Використовуємо Mask 0: (x + y) % 2 == 0
                    mask = (r + col) % 2 == 0
                    
                    # Якщо маска застосована, інвертуємо біт (raw ^ 1), інакше залишаємо
                    yield raw ^ 1 if mask else raw
            
            # Переходимо до наступної пари стовпців і змінюємо напрямок
            c -= 2
            up = not up

    bit_gen = get_bits()
    
    # Зчитуємо 4 біти для перевірки режиму кодування
    mode = "".join(str(next(bit_gen)) for _ in range(4))
    if mode != "0100": # Byte mode
        return ""
        
    # Зчитуємо 8 бітів довжини слова
    size_bits = "".join(str(next(bit_gen)) for _ in range(8))
    size = int(size_bits, 2)
    
    # Зчитуємо відповідну кількість символів (по 8 бітів на кожен)
    msg = []
    for _ in range(size):
        char_bits = "".join(str(next(bit_gen)) for _ in range(8))
        msg.append(chr(int(char_bits, 2)))
        
    return "".join(msg)

def print_qrcode(qrcode_data):
    for row in qrcode_data:
        line_str = ""
        for pixel in row:
            line_str += "██" if pixel == 1 else "  "
        
        print("  " + line_str)
    

